# 32. Make a list of 10 numbers.
# Write a program that uses the min and max functions 
# to print the largest and smallest number in the list.

my_list = [14, -3, 1, 923, -32, 79, 61, 10, -723, 48]

print(max(my_list))
# output:
# 923

print(f'The max is {max(my_list)}')
# output:
# The max is 923

print(min(my_list))
# output:
# -723
